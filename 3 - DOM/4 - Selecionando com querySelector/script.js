// SELECIONANDO ELEMENTOS POR ID COM QUERY SELECTOR
document.querySelector('#')

// SELECIONANDO ELEMENTOS POR CLASSES COM QUERY SELECTOR
document.querySelector('.')

// SELECIONANDO TODOS ELEMENTOS POR CLASSES COM QUERY SELECTOR ALL
document.querySelectorAll('.')

document.querySelector('button').innerHTML = 'Alguma coisa'
document.querySelectorAll('.lista')
document.querySelectorAll('.lista')[1].innerHTML = 'Alterado'

// A propriedade querySelector permite que selecionemos um elemento pelo: ID, TAG HTML, CLASSE